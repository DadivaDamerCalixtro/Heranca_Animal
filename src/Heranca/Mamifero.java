package Heranca;

/**
 *
 * @author IFSC
 */
public class Mamifero {
    String familia;
    String respirar;
    String mamar;
    
    public Mamifero (){
        
    }

    public String getFamilia() {
        return familia;
    }

    public void setFamilia(String familia) {
        this.familia = familia;
    }

    public String getRespirar() {
        return respirar;
    }

    public void setRespirar(String respirar) {
        this.respirar = respirar;
    }

    public String getMamar() {
        return mamar;
    }

    public void setMamar(String mamar) {
        this.mamar = mamar;
    }
}
